using System;
using System.Collections.Generic;
using System.Text;

namespace Calculadora {
   public static class Calculadora {
        
        public static long AdicaoDeProdutos(int numeroUm,int numeroDois) {
            return (numeroUm+ numeroDois);
        }

        public static long SubtracaoDeProdutos(int numeroUm, int numeroDois) {
            return (numeroUm - numeroDois);
        }
        public static long MultiplicacaoDeProdutos(int numeroUm, int numeroDois) {
            return (numeroUm * numeroDois);
        }
        public static long DivisaoDeProdutos(int numeroUm, int numeroDois) {
            return (numeroUm / numeroDois);
        }


    }







}

